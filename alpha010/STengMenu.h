#pragma once

#include <vector>

#include "ALL.h"
#include "SDP.h"

#include "SDLwrapper.h"
#include "TRLengMenu.h"

/*--------------------------------------------------*/
namespace ss
/*--------------------------------------------------*/
{
    /*--------------------------------------------------*/
    class ST_eng_menu : 
    /*--------------------------------------------------*/
        public State
    /*--------------------------------------------------*/
    {
        /*--------------------------------------------------*/
        /*---------------------State Functions--------------*/
        /*--------------------------------------------------*/
    public:
        virtual rt enter()  override;
        virtual rt pause()  override;
        virtual rt resume() override;
        virtual rt exit()   override;

        virtual rt input()  override;
        virtual rt draw()   override;
        /*--------------------------------------------------*/

        /*--------------------------------------------------*/
        /*---------------Constructors/Destructor------------*/
        /*--------------------------------------------------*/
    public:
        /*--------------------------------------------------*/
        ST_eng_menu()
        /*--------------------------------------------------*/
        {
            log("ST_eng_menu()");
            rt ret = init();
        }
        /*--------------------------------------------------*/
        ST_eng_menu(SDL_Renderer* _rend)
        /*--------------------------------------------------*/
        {
            log("ST_eng_menu(SDL_Renderer* "<<_rend<<')');
            rt ret = init();
            this->Rend_ = _rend;
        }
        /*--------------------------------------------------*/
        ~ST_eng_menu()
        /*--------------------------------------------------*/
        {
            log("~ST_eng_menu()");
            rt ret = destroy();
        }
        /*--------------------------------------------------*/

        /*--------------------------------------------------*/
        /*------------------Init/Delete---------------------*/
        /*--------------------------------------------------*/
    private:
        /*--------------------------------------------------*/
        rt init();
        rt destroy();
        /*--------------------------------------------------*/

        /*--------------------------------------------------*/
        /*-----------------Member Variables-----------------*/
        /*--------------------------------------------------*/
    public:
        char const*     text_name = "menu-main";
    private:
        SDL_Renderer*   Rend_;
        TRL_em          TRL_;
        /*--------------------------------------------------*/

        /*--------------------------------------------------*/
        /*-----------------Getter Functions-----------------*/
        /*--------------------------------------------------*/
    public:
        /*--------------------------------------------------*/
        inline void             rend(SDL_Renderer* _rend) 
        /*--------------------------------------------------*/
                                    { Rend_=_rend; }
        /*--------------------------------------------------*/

        /*--------------------------------------------------*/
        /*-----------------Setter Functions-----------------*/
        /*--------------------------------------------------*/
    public:
        /*--------------------------------------------------*/
        inline SDL_Renderer*    rend() const
        /*--------------------------------------------------*/
                                    { return Rend_; }
        /*--------------------------------------------------*/
        inline TRL&             trl()
        /*--------------------------------------------------*/
                                    { return TRL_; }
        /*--------------------------------------------------*/

    }; // END  class ST_eng_menu

} // END    namespace ss
